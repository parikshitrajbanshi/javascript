//////////////upper case//////////

let family='loving mother and  loving father';
console.log(family.toUpperCase());

function first1(str){
    return str.toUpperCase();
}
let result = first1('LOVING MOTHERs');
console.log(result);

/// lower case//////

let family1='LOVING MOTHER AND FATHER';
console.log(family1.toLowerCase());

function first(str){
    return str.toLowerCase();
}
let result1 = first('GJYGVUYTFCHYTRFDCJYTFCJ');
console.log(result1);




